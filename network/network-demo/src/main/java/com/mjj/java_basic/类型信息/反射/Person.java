package com.mjj.java_basic.类型信息.反射;

public class Person {
    /**
     * 私有字段
     */
    private String sex;

    public String age;

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
